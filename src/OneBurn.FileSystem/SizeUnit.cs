namespace OneBurn.FileSystem
{
    public enum SizeUnit
    {
        /// <summary>
        ///     The size unit is automatic determined.
        /// </summary>
        Auto,

        /// <summary>
        ///     The size unit is represented in bytes.
        /// </summary>
        Byte,

        /// <summary>
        ///     The size unit is represented in kilobytes.
        /// </summary>
        Kilobyte,

        /// <summary>
        ///     The size unit is represented in megabytes.
        /// </summary>
        Megabyte,

        /// <summary>
        ///     The size unit is represented in gigabytes.
        /// </summary>
        Gigabyte,

        /// <summary>
        ///     The size unit is represented in terabytes.
        /// </summary>
        Terabyte,

        /// <summary>
        ///     The size unit is represented in petabytes.
        /// </summary>
        Petabyte,

        /// <summary>
        ///     The size unit is represented in exabytes.
        /// </summary>
        Exabyte,

        /// <summary>
        ///     The size unit is represented in zettabytes.
        /// </summary>
        Zettabyte,

        /// <summary>
        ///     The size unit is represented in yottabytes.
        /// </summary>
        Yottabyte
    }
}